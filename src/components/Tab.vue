                <h1>Rhinoplastie :&nbsp;Qu'est-ce que ce ?</h1>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le terme de «rhinoplastie» désigne la modification de la morphologie du nez, dans un but d’amélioration esthétique, et parfois, fonctionnel (correction d’éventuels problèmes de respiration nasale). La chirurgie du nez est une des interventions la plus demandée en France.&nbsp;</span></p>
                <h2 style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><br>Le déroulement de la rhinoplastie</span></h2>
                            <span style="font-size: 12pt;">L’intervention vise à remodeler le nez pour l’embellir. Il s’agit de corriger spécifiquement les disgrâces présentes, qu’elles soient congénitales apparues à l’adolescence, consécutives à un traumatisme, ou dues au processus de vieillissement.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt; text-align: justify;">Le principe est, à partir d’incisions dissimulées dans les narines, de remodeler l’os et le cartilage qui constituent l’infrastructure solide du nez et lui confèrent sa forme particulière. La peau recouvrant le nez devra se réadapter et se redraper grâce à son élasticité sur cette charpente ostéo-cartilagineuse qui a été modifiée. Ce dernier point souligne l’importance de la qualité de la peau dans l’obtention du résultat final. On comprend ainsi qu’une rhinoplastie ne laisse pas habituellement de cicatrice visible sur la peau.&nbsp;</span><span style="font-size: 12pt;"><br></span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le but est d’obtenir un nez d’aspect naturel, s’harmonisant dans ses rapports avec les autres traits du visage, convenant à la psychologie et à la personnalité du patient, et répondant aux demandes de ce dernier.&nbsp;</span></div>
                            <div style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Lorsqu’une obstruction nasale gêne la respiration, elle pourra être traitée dans le même temps opératoire, qu’elle soit due à une déviation de la cloison ou à une hypertrophie des cornets (formations osseuses présentes dans les fosses nasales).&nbsp;</span></div>
                            <h2><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><br>Précautions à prendre</span></h2>
                            <div style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">L’intervention, pratiquée aussi bien chez la femme que chez l’homme, peut être effectuée dès la fin de la croissance, c’est-à-dire à partir d’environ 16 ans.&nbsp;Une rhinoplastie peut être réalisée isolément ou être associée, si nécessaire, à d’autre gestes complémentaires au niveau de la face, en particulier à une génioplastie (modification du menton, parfois réalisée dans le même temps opératoire pour améliorer l’ensemble du profil).&nbsp;</span></div>
                            <div style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention.&nbsp;Elle peut exceptionnellement être prise en charge par l’assurance maladie sous certaines conditions.Dans de rares cas, une amélioration de la morphologie du nez peut être obtenue par des techniques non chirurgicales, que votre chirurgien vous proposera si cette solution est envisageable dans votre cas particulier.&nbsp;<br></span></div>
                            <div style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention.</span></div>
                            <div style="text-align: justify;"></div>
                            <div style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">L’arrêt du tabac est vivement recommandé avant l’intervention.</span></div>
                            <p><em style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><span>Il est fondamental de rester à jeun (ne rien manger ni boire) 6 heures avant l’intervention</span></em><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">.</span></p>

            <div><br>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b>Avant l'intervention&nbsp;</b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Avant toutes choses, les motivations et les demandes du (de la) patient(e) sont analysées. Après, une étude attentive de la pyramide nasale et de ses rapports avec le reste du visage est faite, ainsi qu’un examen endo-nasal.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le but est de définir le résultat «idéal» adapté au reste du visage, aux désirs et à la personnalité du (de la ) patient(e).&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le chirurgien, ayant bien compris la demande du (de la) patient(e), devient son guide dans le choix du résultat futur et de la technique à employer.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Il peut parfois déconseiller l’intervention.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le résultat escompté pourra être simulé par retouches de photographies ou morphing informatique. L’image virtuelle, ainsi obtenue ne constitue qu’un projet qui peut aider dans la compréhension des attentes des patients. Cependant, on ne peut en aucune manière s’engager à ce que le résultat réalisé lui soit en tout point superposable.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le but est d’obtenir un nez d’aspect naturel, s’harmonisant dans ses rapports avec les autres traits du visage, convenant à la psychologie et à la personnalité du patient, et répondant aux demandes de ce dernier.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention.</span></p>
                <p style="text-align: justify;"></p>
                <p style="text-align: justify;"></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b>Type d'anesthésie et modalités d'hospitalisation </b><b>lors d'une rhinoplastie</b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : Habituellement l’intervention se pratique sous anesthésie générale. Toutefois, dans certains cas, une anesthésie locale approfondie par des tranquillisants administrés par voie intra-veineuse (anesthésie « vigile ») pourra suffire.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le choix entre ces différentes techniques sera le fruit d’une discussion entre vous, le chirurgien et l’anesthésiste.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : L’intervention peut se pratiquer en «ambulatoire», c’est-à-dire avec une sortie le jour même après quelques heures de surveillance. Toutefois, selon les cas, une courte hospitalisation peut être préférable. L’entrée s’effectue alors le matin (ou parfois la veille) et la sortie est autorisée dès le lendemain ou le surlendemain.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b><i><br></i>L'intervention: l'opération du nez</b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Chaque chirurgien adopte des procédés qui lui sont propres et qu’il adapte à chaque cas pour corriger électivement les défauts en présence et obtenir les meilleurs résultats. Il est donc difficile de systématiser l’intervention. Toutefois, on peut en retenir des principes de base communs :</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><em>Incisions :</em> Elles sont dissimulées, le plus souvent à l’intérieur des narines ou sous la lèvre supérieure, et il n’en résulte donc aucune cicatrice visible à l’extérieur.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Parfois, pourtant, des incisions externes peuvent être requises : elles sont en travers de la columelle (pilier séparant les deux narines) afin de réaliser une rhinoplastie «ouverte» ou cachées à la base des ailes du nez si l’on doit réduire la taille des narines.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><em>Rectifications :</em> L’infrastructure ostéo-cartilagineuse peut alors être refaçonnée selon le programme établi. Cette étape fondamentale peut mettre en oeuvre une infinité de procédés dont le choix se fera en fonction des anomalies à corriger et des préférences techniques du chirurgien. On pourra ainsi rétrécir un nez trop large, réaliser l’ablation d’une bosse, corriger une déviation, affiner une pointe, raccourcir un nez trop long, redresser une cloison. Parfois, des greffons cartilagineux ou osseux seront utilisés pour combler une dépression, soutenir une portion du nez ou améliorer la forme de la pointe.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><em>Sutures :</em> Les incisions sont refermées avec de petits fils, le plus souvent résorbables.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><em>Pansements et attelles :</em> Les fosses nasales peuvent être méchées avec différents matériaux absorbants. Un pansement modelant est souvent réalisé à la surface du nez à l’aide de petites bandelettes adhésives. Enfin, une attelle de maintien et de protection, en plâtre ou en matière plastique ou métallique, est moulée et fixée sur le nez, pouvant parfois remonter sur le front.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">En fonction du chirurgien, de l’ampleur des améliorations à apporter, et de la nécessité éventuelle de gestes complémentaires, l’intervention peut durer de 45 minutes à deux heures.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b><i><br>Après l'intervention : Les suites opératoires</i></b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Les suites sont rarement douloureuses et c’est plutôt l’impossibilité de respirer par le nez (du fait de la présence des mèches) qui constitue le principal désagrément des premiers jours.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">On observe, surtout au niveau des paupières, l’apparition d’un oedème (gonflement) et parfois d’ecchymoses (bleus) dont l’importance et la durée sont très variables d’un individu à l’autre.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Il est recommandé de se reposer et de ne faire aucun effort les jours suivant l’intervention.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Les mèches sont ôtées entre le 1er et le 5ème jour post-opératoires. L’attelle est retirée entre le 5ème et le 8ème jour, où elle sera parfois remplacée par une nouvelle attelle plus petite pour encore quelques jours.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le nez apparaîtra alors encore assez massif du fait de l’oedème et une gêne respiratoire sera encore présente, due au gonflement de la muqueuse et à la formation possible de croûtes dans les fosses nasales.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Les stigmates de l’intervention vont s’atténuer progressivement, permettant le retour à une vie socio-professionnelle&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">normale après quelques jours (10 à 20 jours selon les cas).</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Les sports et activités violentes sont à éviter les 3 premiers mois.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b><i><br></i>Les résultats attendus de la rhinoplastie</b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Ce résultat est, le plus souvent, conforme aux désirs du patient et assez proche du projet établi en préopératoire.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Les modifications apportées par une rhinoplastie sont définitives, et seules surviendront des modifications mineures et tardives en rapport avec le processus naturel de vieillissement (comme pour un nez non opéré).&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Le but de cette chirurgie est d’apporter une amélioration et non pas d’atteindre la perfection. Si vos souhaits sont réalistes, le résultat obtenu devrait vous donner une grande satisfaction.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt; background-color: #ffffff;">Un délai de deux à trois mois est nécessaire pour avoir un bon aperçu du résultat, en sachant que l’aspect définitif ne sera obtenu qu’après six mois à un an de lente et subtile évolution.&nbsp;</span><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">L’intervention apporte une amélioration morphologique et esthétique le plus souvent tout à fait appréciable, ainsi qu’un bénéfice psychologique non négligeable.</span></p>
                <p style="text-align: justify;"></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b>Les imperfections de résultat&nbsp;</b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Elles peuvent résulter d’un malentendu concernant les buts à atteindre ou survenir du fait de phénomènes cicatriciels inhabituels ou de réactions tissulaires inattendues (mauvais redrapage spontané de la peau, fibrose rétractile).</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Ces petites imperfections, si elles sont mal supportées, pourront éventuellement être corrigées par une retouche chirurgicale, en général beaucoup plus simple que l’intervention initiale, tant du point de vue technique que des suites opératoires. Une telle retouche ne peut toutefois pas être réalisée avant plusieurs mois afin d’agir sur des tissus stabilisés et ayant atteint une bonne maturation cicatricielle.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><b><i><br>Les risques et complications envisageables</i></b></span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Une rhinoplastie, bien que réalisée pour des motivations essentiellement esthétiques, n’en reste pas moins une véritable intervention chirurgicale, ce qui implique les risques liés à tout acte médical, aussi minime soit-il.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Il faut distinguer les complications liées à l’anesthésie de celles liées au geste chirurgical.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">En ce qui concerne l’anesthésie, lors de la consultation, le médecin anesthésiste informera lui-même le patient des risques anesthésiques. Il faut savoir que l’anesthésie induit dans l’organisme des réactions parfois imprévisibles, et plus ou moins faciles à maîtriser : le fait d’avoir recours à un anesthésiste parfaitement compétent, exerçant dans un contexte réellement chirurgical, fait que les risques encourus sont devenus statistiquement très faibles.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Il faut savoir, en effet, que les techniques, les produits anesthésiques et les méthodes de surveillance ont fait d’immenses progrès ces trente dernières années, offrant une sécurité optimale, surtout quand l’intervention est réalisée en dehors de l’urgence et chez une personne en bonne santé.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">En ce qui concerne le geste chirurgical : en choisissant un Chirurgien Plasticien qualifié et compétent, formé à ce type d’intervention, vous limitez au maximum ces risques, sans toutefois les supprimer complètement.&nbsp;</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Heureusement, les vraies complications sont rares à la suite d’une rhinoplastie réalisée dans les règles. En pratique, l’immense majorité des interventions se passe sans aucun problème et les patient(e)s sont pleinement satisfait(e)s de leur résultat.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Pour autant, et malgré leur rareté, vous devez être informé(e) des complications possibles:</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><strong>• Saignements :</strong> ils sont possibles les premières heures mais restent habituellement très modérés. Quand ils sont trop importants, cela peut justifier un nouveau méchage plus poussé, voire une reprise au bloc opératoire.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">• <b>Hématomes </b>: ils peuvent nécessiter une évacuation s’ils sont volumineux ou trop douloureux.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">• <b>Infection </b>: malgré la présence naturelle de microbes dans les fosses nasales, elle est très rare. Le cas échéant, elle justifie rapidement un traitement approprié.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">• <b>Cicatrices inesthétiques </b>: elles ne peuvent concerner que les cicatrices externes (quand elles existent) et ne sont que très rarement disgracieuses au point de nécessiter une retouche.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">• <b>Atteintes cutanées </b>: bien que rares, elles sont toujours possibles, souvent par le fait de l’attelle nasale. Les simples plaies ou érosions cicatrisent spontanément sans laisser de traces, contrairement aux nécroses cutanées, heureusement exceptionnelles, qui laissent souvent une petite zone de peau cicatricielle.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Au total, il ne faut pas surévaluer les risques, mais simplement prendre conscience qu’une intervention chirurgicale, même apparemment simple, comporte toujours une petite part d’aléas. Le recours à un Chirurgien Plasticien qualifié vous assure que celui-ci a la formation et la compétence requise pour savoir éviter ces complications, ou les traiter efficacement le cas échéant.</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><br>Tels sont les éléments d’information que nous souhaitions vous apporter en complément à la consultation. Nous vous conseillons de conserver ce document, de le relire après la consultation et d’y réfléchir « à tête reposée ».</span></p>
                <p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Cette réflexion suscitera peut-être de nouvelles questions, pour lesquelles vous attendrez des informations complémentaires. Nous sommes à votre disposition pour en reparler au cours d’une prochaine consultation, ou bien par téléphone, voire le jour même de l’intervention où nous nous reverrons, de toute manière, avant l’anesthésie.<b>&nbsp;</b></span></p>
