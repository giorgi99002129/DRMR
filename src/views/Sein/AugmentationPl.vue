                    <div style="text-align: justify;">
                <h1>Augmentation mammaire</h1>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><span style="font-size: 12pt;">L’hypoplasie mammaire est définie par un volume de seins insuffisamment développés par rapport à la morphologie de la patiente. Elle peut être la conséquence d’un développement insuffisant de la glande à la puberté, ou apparaître secondairement par perte du volume glandulaire (grossesse, amaigrissement, perturbations hormonales…). Le manque de volume peut aussi être associé à une ptôse (poitrine «tombante» avec affaissement de la glande, distension de la peau et aréoles trop basses).</span> <br><br><strong></strong></span></span>
                <h2>Objectifs et principes d'intervention</h2>
                <span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><strong></strong><br><span style="font-size: 12pt;">L’intervention peut se pratiquer à tout âge à partir de 18 ans. Une patiente mineure n’est habituellement pas estimée apte à subir une augmentation mammaire esthétique. Elle est cependant possible dans les cas d’hypoplasies sévères ou dans le cadre de reconstruction comme les seins tubéreux ou les agénésies mammaires. Cette chirurgie à visée purement esthétique ne peut bénéficier d’une prise en charge par l’assurance maladie. Seuls quelques rares cas d’agénésie mammaire vraie (absence radicale de tout développement mammaire) peuvent parfois espérer une participation de la sécurité sociale après entente préalable.</span></span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><span style="font-size: 12pt;"> Les implants mammaires actuellement utilisés sont composés d’une enveloppe et d’un produit de remplissage. L’enveloppe est toujours constituée d’un élastomère de silicone. En revanche, les prothèses diffèrent par leur contenu, c’est-à-dire le produit de remplissage contenu au sein de l’enveloppe. L’implant est dit prérempli lorsque le produit de remplissage a été incorporé en usine (gel et/ou sérum physiologique). La gamme des différents volumes est donc fixée par le fabricant. Les implants gonflables au sérum physiologique sont remplis par le chirurgien qui peut adapter dans une certaine mesure le volume de la prothèse pendant l’intervention.</span><br><br> </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><strong>Les implants préremplis de gel de silicone de nouvelle génération&nbsp;<br><br></strong></span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><span style="font-size: 12pt;"> La grande majorité des prothèses actuellement posées en France et dans le monde sont préremplies de gel de silicone. Cette hypotrophie est souvent mal acceptée physiquement et psychologiquement par la patiente qui le vit comme une atteinte à sa féminité, avec pour corollaire une altération de la confiance en soi et un mal-être, parfois profond, pouvant aller jusqu’au véritable complexe. C’est pourquoi, l’intervention se propose d’accroître le volume d’une poitrine jugée trop petite grâce à l’implantation de prothèses. Société Française de Chirurgie Plastique Reconstructrice et Esthétique</span><br><br> </span></div>
                <div style="text-align: justify;">
                <h3>Plastie d'augmentation mammaire par prothèse</h3>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Ces implants, utilisés depuis plus de 40 ans, ont fait la preuve de leur innocuité et de leur excellente adaptation à ce type de chirurgie car ils sont très proches de la consistance d’un sein normal. Ils ont par ailleurs bien évolué, en particulier à la fin des années 1990, afin de corriger les faiblesses que l’on pouvait leur reprocher. Aujourd’hui, tous les implants disponibles en France sont soumis à des normes précises et rigoureuses : marquage CE (communauté européenne) + autorisation de l’ANSM (Agence Nationale de Sécurité du Médicament et des produits de santé).</span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Ils sont composés de gel de silicone souple, entouré d’une enveloppe étanche, solide et élastique en élastomère de silicone qui peut être lisse ou texturée (rugueuse). Les évolutions marquantes des nouveaux implants, leur conférant une meilleure fiabilité, concernent tant les enveloppes que le gel lui-même : </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• les enveloppes, dont la paroi est aujourd’hui beaucoup plus solide, empêchent la « transpiration» du gel vers l’extérieur (qui était une source importante de coques) et ont une résistance à l’usure très supérieure ;</span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • les gels de silicone « cohésifs », dont la consistance est moins fluide, limitent beaucoup le risque de se répandre en cas de rupture de l’enveloppe. </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 10pt;"><span style="font-size: 12pt;">À côté de cette amélioration de la fiabilité, la nouvelle génération d’implants en silicone se caractérise aussi par la grande diversité de formes actuellement disponibles, permettant une adaptation personnalisée à chaque cas. Ainsi, à côté des classiques prothèses rondes sont apparus des implants «anatomiques » profilés en forme de goutte d’eau, plus ou moins hauts, larges ou projetés. Cette grande diversité de formes, associée à un large choix de volumes, permet d’optimiser et d’adapter le choix, presque « sur mesure», des prothèses en fonction de la morphologie de la patiente et de ses attentes personnelles.</span><br><br> </span></div>
                <div style="text-align: justify;">
                <h3>Les autres types d'implant mammaire</h3>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Les enveloppes des prothèses sont toujours en élastomère de silicone, c’est le produit de remplissage qui diffère. À ce jour en France, seules deux alternatives au gel de silicone sont autorisées : Sérum physiologique : Il s’agit d’eau salée (constituant à 70 % du corps humain). Ces prothèses peuvent être «préremplies» (en usine) ou «gonflables » (par le chirurgien durant l’intervention). Du fait de leur contenu liquidien (et non gélatineux), elles ont une consistance peu naturelle, forment beaucoup plus de «plis » perceptibles au toucher, voire visibles, et peuvent souvent être victimes de dégonflement brutal et parfois précoce. Hydrogel : C’est la toute dernière substance ayant reçu en 2005 son homologation par l’Afssaps. Il s’agit d’un gel aqueux, composé en grande partie d’eau gélifiée par un dérivé de cellulose. Ce gel, de consistance plus naturelle que le sérum physiologique, est lui aussi résorbable par l’organisme en cas de rupture de l’enveloppe. Il existe enfin des prothèses dont l’enveloppe de silicone est recouverte de polyuréthane ce qui pourrait contribuer à réduire l’incidence des phénomènes de coque.</span></div>
