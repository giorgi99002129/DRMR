                    <div style="text-align: justify;">
                <h1>Gynécomastie chez l'homme</h1>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Une gynécomastie est définie par une augmentation du volume de la glande mammaire chez l’homme. Elle correspond à une hyperplasie uni- ou bilatérale. Généralement elle est idiopathique, c’est à dire qu’il n’existe aucune cause à sa survenue; cependant, dans certains cas elle peut être en rapport avec une production hormonale anormale, ou liée à la prise de certains médicaments. Un bilan est nécessaire afin d’éliminer une cause éventuelle. Ce bilan aura pour but de doser les différentes hormones connues, de rechercher une tumeur testiculaire ou hypophysaire. </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Une mammographie ou une échographie peut être demandée afin d’analyser la densité de la glande, ou d’éliminer un cancer du sein chez l’homme plus âgé, notamment en cas de forme unilatérale. Si une cause est retrouvée, elle devra faire l’objet d’un traitement spécifique. En effet, lorsqu’une étiologie est retrouvée, le traitement de celle-ci peut permettre une régression plus ou moins complète de la gynécomastie. L’augmentation du volume mammaire chez l’homme, notamment en période d’adolescence, est souvent mal vécue et peut poser de nombreux problèmes psychologiques. Cette atteinte physique chez l’adolescent, au moment même où il construit son image d’homme, peut entraîner un repli sur lui-même, voire un véritable complexe. De plus, cette gynécomastie peut s’avérer douloureuse. D’un point de vue anatomique et physiopathologique, la glande mammaire existe chez l’homme normal, mais elle est de type infantile, réduite à un simple bourgeon mammaire. Dans quelques cas, cette glande mammaire peut se développer si l’environnement hormonal est propice. Lorsqu’aucune cause n’a été retrouvée et si le patient est gêné, une intervention chirurgicale peut être proposée, à condition que le patient soit en bonne forme physique et psychique. On appelle ce type d’intervention une « cure de gynécomastie ».<br> </span></div>
                <div style="text-align: justify;">
                <h3>A quoi ressemble une gynécomastie</h3>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Il s’agit d’un développement de la glande mammaire centré sur l’aréole, le plus souvent bilatéral et symétrique, de consistance ferme et sensible à la palpation. Les gynécomasties sont à différencier des adipomasties qui sont beaucoup plus fréquentes et correspondent à une accumulation locale de graisse. Ces adipomasties ne sont pas centrées par rapport à l’aréole, leur consistance est molle, insensible, bilatérale, symétrique, mais les deux peuvent être associées (adipogynécomastie). Chez l’adolescent peut apparaître une gynécomastie transitoire, liée à un déséquilibre hormonal en faveur des œstrogènes, disparaissant en quelques mois. Les cancers du sein ont une présentation différente; il s’agit de forme rare, touchant l’homme généralement après 40 ans. Ce sont des lésions unilatérales, dures, insensibles, associées à des déformations ou rétractions du mamelon voire un écoulement sanglant.</span></div>
                <div style="text-align: justify;">
                <h3>Objectifs et principes de l’intervention</h3>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Le but de la chirurgie correctrice est de rétablir au mieux l’anatomie normale avec pour principes de : </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• réduire le volume mammaire par exérèse chirurgicale directe (mastectomie sous-cutanée) pour les formes glandulaires, ou par lipoaspiration pour les formes graisseuses. Dans les cas de formes mixtes, le chirurgien doit alors combiner les deux techniques, </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• diminuer l’excédent cutané : généralement, la diminution du volume glandulaire va permettre la rétraction cutanée. Cette rétraction cutanée est favorisée par la lipoaspiration mais elle est d’autant plus nette qu’il s’agit d’un homme jeune et que la peau est de bonne qualité (peau ferme, élastique, sans vergeture). Certains gestes post-opératoires comme les&nbsp;massages pourront aider la rétraction cutanée. Dans certains cas, lorsque l’excédent cutané est trop important, le chirurgien peut être amené à le réduire, mais au prix de cicatrices sur la peau. Cette plastie de réduction cutanée peut être réalisée dès la première intervention ou secondairement. </span></div>
                <div style="text-align: justify;">
                <h3>Avant l'intervention</h3>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le bilan à la recherche d’une cause à la gynécomastie a été évoqué précédemment. Chez les hommes obèses ou en surpoids, un régime associé à des exercices physiques sont instaurés car une perte de poids peut faire régresser ou même faire disparaître la gynécomastie (adipomastie). En fonction du contexte anatomique, des préférences et des habitudes du chirurgien, et des désirs exprimés par le patient, une stratégie opératoire aura été décidée. Seront ainsi prédéterminés la technique opératoire employée et l’emplacement des cicatrices (cf. paragraphe « L’intervention »). Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions.</span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Le médecin anesthésiste sera vu en consultation, au plus tard 48 heures avant l’intervention. Votre chirurgien et l’anesthésiste devront être tenus au courant de tous les médicaments que vous prenez. En effet, certains d’entre eux peuvent interférer avec l’anesthésie ou favoriser les saignements. Sauf cas particulier, aucun médicament contenant de l’aspirine ou des anti-inflammatoires ne devront être pris dans les dix jours précédant l’opération. L’arrêt du tabac est recommandé au moins un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation). </span></div>
                <div style="text-align: justify;">
                <h3>Type d'anesthésie et modalités d'hospitalisation</h3>
                </div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : il s’agit le plus souvent d’une anesthésie générale classique, durant laquelle vous dormez complètement. Dans certains cas, une anesthésie «vigile» (anesthésie locale approfondie par des tranquillisants administrés par voie intraveineuse) ou même une anesthésie locale simple pourront cependant être utilisées (à discuter avec le chirurgien et l’anesthésiste). </span></div>
                <div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : l’intervention nécessite habituellement une hospitalisation de 24 heures à 48 heures. La durée d’hospitalisation est cependant fonction de la technique chirurgicale employée. En effet une lipoaspiration simple nécessitera une hospitalisation plus courte qu’une mastectomie sous-cutanée. L’entrée s’effectue généralement le matin ou parfois la veille dans l’après-midi. Dans certains cas, l’intervention peut se pratiquer en « ambulatoire », c’est-à-dire avec une sortie le jour même après quelques heures de surveillance.</span></div>
