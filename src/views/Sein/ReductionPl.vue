                    <div style="text-align: justify;">
<h1>Réduction mammaire ou hyperthrophie mammaire</h1>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> L’hypertrophie mammaire est définie par un volume des seins trop important, notamment par rapport à la morphologie de la patiente. Cet excès de volume est en général associé à un affaissement des seins (<a href="/web/20190213055125/https://www.paris-esthetics.com/fr/categories/Mastopexie">ptose mammaire</a>) et parfois à un certain degré d’asymétrie. L’hypertrophie mammaire implique presque toujours un retentissement physique et fonctionnel (douleurs du cou, des épaules et du dos, gêne pour la pratique des sports, difficultés vestimentaires).</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Il existe aussi fréquemment un retentissement psychologique notable. Ces troubles justifient la participation financière de l’assurance maladie sous certaines conditions.<br><br> </span></div>
<div style="text-align: justify;">
<h2>Objectifs et principes d'intervention</h2>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> L’intervention réalise l’ablation du tissu mammaire en excès. On conserve un volume en harmonie avec la silhouette de la patiente et conforme à ses désirs. Ce volume mammaire résiduel est ascensionné, concentré et remodelé. Il faut ensuite adapter l’enveloppe cutanée, ce qui impose de retirer la peau en excès de manière à assurer une bonne tenue et un bon galbe aux nouveaux seins.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Les berges de la peau ainsi découpées sont alors suturées : ces sutures sont à l’origine des cicatrices. Souvent ces cicatrices ont la forme d’un T inversé avec trois composantes : péri-aréolaire au pourtour de l’aréole entre la peau brune et la peau blanche, verticale, entre le pôle inférieur de l’aréole et le sillon sous-mammaire, horizontale, dissimulée dans le sillon sous-mammaire. La longueur de la cicatrice horizontale est proportionnelle à l’importance de l’hypertrophie et de la ptose. </span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Parfois, notamment lorsque l’hypertrophie et la ptose sont modérées, on peut réaliser une méthode dite « verticale» qui permet de supprimer la cicatrice transversale dans le sillon sous-mammaire et de réduire la rançon cicatricielle à ses composantes péri-aréolaire et verticale.&nbsp;Une plastie mammaire pour hypertrophie peut être effectuée à partir de la fin de la croissance et au-delà, pendant toute la durée de la vie. Une grossesse ultérieure est bien évidemment possible ainsi qu’un allaitement, mais on conseille d’attendre au moins six mois après l’intervention. Le risque de survenue d’un cancer n’est pas augmenté par cette intervention. Il serait même légèrement réduit. </span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"><span style="font-size: 12pt;"><strong><br></strong></span></span>
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions. Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention. Outre les examens pré-opératoires habituels, un bilan radiologique du sein est prescrit (mammographie, échographie). L’arrêt du tabac est vivement recommandé, au moins un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation). L’arrêt d’une éventuelle contraception orale peut être requis, notamment en cas de facteur de risques associés (obésité, mauvais état veineux, trouble de la coagulation). Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. </span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Type d’anesthésie : Il s’agit d’une anesthésie générale, durant laquelle vous dormez complètement.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Modalités d’hospitalisation : Une hospitalisation de un à trois jours est habituellement nécessaire.</span></div>
