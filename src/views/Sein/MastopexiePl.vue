                    <div style="text-align: justify;">
<h1>Ptose mammaire ou mastopexie: qu'est-ce que c'est?</h1>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> La ptose mammaire est définie par un affaissement du sein et une distension de la peau qui l’enveloppe. Le sein est en position trop basse et de plus fréquemment «déshabité» dans sa partie supérieure. La ptose peut exister d’emblée mais survient le plus souvent après un amaigrissement important ou au décours d’une grossesse avec allaitement. Elle peut être isolée : on parle alors de ptose pure. Elle peut aussi être associée à un certain degré d’hypertrophie mammaire. On peut à l’inverse observer une ptose mammaire dans le contexte d’un sein trop petit (hypoplasie ou hypotrophie mammaires). Ces défauts esthétiques ne justifient pas une prise en charge par l’Assurance-Maladie.<br><br> </span></div>
<div style="text-align: justify;">
<h2>Objectifs et principes d'intervention</h2>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> L’opération consiste à remodeler le sein en agissant sur l’enveloppe cutanée et sur le tissu glandulaire. La glande est concentrée et placée en bonne position. Il faut ensuite adapter l’enveloppe cutanée, ce qui impose de retirer la peau en excès de manière à assurer une bonne tenue et une belle forme au nouveau sein. Ces gestes permettent de replacer l’aréole et le mamelon qui étaient trop bas situés. Les berges de la peau qui ont été découpées sont suturées en fin d’intervention : ces sutures sont à l’origine des cicatrices. En cas de ptose très importante, la cicatrice à la forme d’un T inversé avec trois composantes : péri-aréolaire au pourtour de l’aréole entre la peau brune et la peau blanche, verticale entre le pôle inférieur de l’aréole et le sillon sous mammaire, horizontale dissimulée dans le sillon sous mammaire. La longueur de la cicatrice horizontale est proportionnelle à l’importance de la ptose.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> L’intervention chirurgicale a pour but de replacer l’aréole et le mamelon en bonne position, de reconcentrer et d’ascensionner la glande et de retirer la peau excédentaire afin d’obtenir deux seins harmonieux, joliment galbés et ascensionnés.<br><br></span></div>
<div style="text-align: justify;">
<h3>Chirurgie de la ptose mammaire</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Plus souvent, en présence d’une ptose mammaire modérée, on peut réaliser une méthode dite «verticale» qui permet de supprimer la cicatrice transversale dans le sillon sous mammaire et de réduire la rançon cicatricielle à ses composantes périaréolaire et verticale. Dans certains cas de ptose mammaire très modérée, il est possible d’utiliser une technique qui permet d’effectuer la correction de l’affaissement uniquement avec une cicatrice autour de l’aréole. Enfin, lorsque la ptose est associée à une insuffisance de volume (hypoplasie mammaire), il peut être souhaitable de mettre en place, dans le même temps opératoire, une prothèse pour redonner au sein un volume satisfaisant, ou un complément graisseux pour améliorer le décolleté. Dans ce cas, la surface de peau enlvée est moins importante et donc les cicatrices plus courtes. Il est habituellement possible de retirer l’excès de peau autour de l’aréole et de limiter ainsi la cicatrice uniquement à un cercle péri-aréolaire. Une plastie mammaire pour ptose peut être effectuée à partir de la fin de la croissance et, au-delà, pendant toute la durée de la vie. Une grossesse ultérieure est bien évidemment possible, ainsi qu’un allaitement, mais on conseille d’attendre au moins six mois après l’intervention. Le risque de survenue d’un cancer n’est pas augmenté par cette intervention.</span></div>
<div style="text-align: justify;">
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions. Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention. Outre les examens pré-opératoires habituels, un bilan radiologique du sein est prescrit (mammographie, échographie). L’arrêt de tabac est vivement recommandé, au moins un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation). L’arrêt d’une éventuelle contraception orale peut être requis, notamment en cas de facteur de risques associés (obésité, mauvais état veineux ; trouble de la coagulation). Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. </span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation&nbsp;</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Type d’anesthésie : Il s’agit d’une anesthésie générale, durant laquelle vous dormez complètement.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"> Modalités d’hospitalisation : Une hospitalisation d’un à deux jours est habituellement nécessaire.</span></div>
