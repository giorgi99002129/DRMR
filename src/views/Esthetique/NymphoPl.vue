								<div style="text-align: justify;">
<h1>Nymphoplastie-hypertrophie des petites lèvres</h1>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><br>L’hypertrophie des petites lèvres est définie par une taille excessive des petites lèvres notamment par rapport à celle des grandes lèvres. Ainsi en position debout les petites lèvres font saillie et dépassent de la fente vulvaire, ce qui fait dire aux patientes qu’elles ont des petites lèvres « pendantes ». L’hypertrophie est le plus souvent bilatérale conservant l’asymétrie physiologique des petites lèvres, la gauche étant naturellement plus importante que la droite ; elle peut cependant être unilatérale. Cet aspect apparaît le plus souvent à la puberté (hypertrophie primaire juvénile) mais il peut survenir après un accouchement ou à la ménopause (hypertrophie secondaire par vulvoptose ou fausse hypertrophie dont le traitement n’est pas alors celui de l’hypertrophie classique).</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Bien souvent les petites lèvres vont présenter une hyperpigmentation due aux frottements dans la lingerie. Parfois, l’hypertrophie se prolonge en avant réalisant des replis de part et d’autre du clitoris. L’hypertrophie des petites lèvres entraîne souvent une gêne vestimentaire (port de jean serré, de string, de maillot de bain moulant) ou lors de la pratique de certains sports (bicyclette, équitation, varappe). La gêne est variable lors des rapports sexuels, moins physique (interposition des petites lèvres lors de la pénétration) que psychologique (gêne à se dénuder devant un partenaire). Parfois cette hypertrophie est responsable de mycoses à répétition. Ces troubles peuvent justifier la prise en charge par l’assurance maladie dans les cas les plus importants.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h2>Objctifs de l'intervention</h2>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’intervention chirurgicale ou nymphoplastie a pour but la réduction de la taille des petites lèvres, la correction d’une éventuelle asymétrie majeure, sans omettre, s’il y a lieu, la réduction des prolongements antérieurs de chaque côté du clitoris. L’objectif est d’obtenir une vulve harmonieuse avec une taille des petites lèvres en rapport avec celle des grandes lèvres et du volume de la vulve.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Principes de l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’intervention réalise l’ablation de la muqueuse en excès. Plusieurs techniques chirurgicales ont été décrites. La plus simple consiste en une résection à la demande selon un schéma pré-établi personnalisé, dont le tracé suit le bord libre de la lèvre. Cette technique a l’avantage d’être sûre, modulable. Elle permet de traiter non seulement l’hypertrophie des petites lèvres mais aussi le prolongement antérieur s’il y a lieu, et de réduire l’hyperpigmentation fréquemment associée. Les berges muqueuses ainsi découpées sont alors suturées au fil résorbable. D’autres méthodes ont été décrites : résection cuneïforme ou méthode chinoise (par fenestration,) mais elles ne présentent pas la même adaptabilité que la méthode précédente et doivent être réservées à certains cas particuliers. Une nymphoplastie peut être effectuée dès l’âge adulte et sans limite d’âge. L’intervention n’aura aucune conséquence négative ultérieure sur les rapports sexuels ni sur les accouchements.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Un bilan pré-opératoire est réalisé conformément aux prescriptions. Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’arrêt du tabac est vivement recommandé, au moins un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation).</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’arrêt d’une éventuelle contraception orale peut être requis, notamment en cas de facteurs de risques associés (obésité, mauvais état veineux ; troubles de la coagulation).</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Il est fondamental de rester à jeun (ne rien manger ni boire) 6 heures avant l’intervention.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le rasage n’est pas nécessaire.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : la nymphoplastie est le plus souvent réalisée sous anesthésie vigile c’est-à-dire une anesthésie locale approfondie par des tranquilisants administrés par voie intraveineuse.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : en général, l’intervention se pratique en ambulatoire c’est-à-dire avec une sortie le jour même après quelques heures de surveillance. La patiente peut alors regagner son domicile dès que sont état général le permet. Toutefois, pour des raisons sociales, familiales ou personnelles une courte hospitalisation peut être envisagée.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>L’intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Chaque chirurgien adopte une technique qui lui est propre et qu’il adapte à chaque cas pour obtenir les meilleurs résultats. En fin d’intervention, un pansement léger est placé dans un slip de protection. En fonction du chirurgien et du cas clinique l’intervention peut durer de 30 à 60 minutes.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Après l'intervention : Les suites opératoires</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Chaque chirurgien a son protocole et le propose à sa patiente. Un minime saignement dure 2 à 3 jours. Un gonflement et des ecchymoses sont habituels. Les suites opératoires sont en général peu douloureuses, ne nécessitant que des antalgiques simples. Une protection sera glissée dans le slip. Il est conseillé d’adopter un habillement ample (jupe ou pantalon peu serré).La toilette intime est réalisée par bains de siège bi- quotidiens avec un antiseptique habituel. On préférera pour le séchage de la zone opérée un séchoir à cheveux très doux. Les fils de sutures se résorberont en principe en huit à douze jours, date à laquelle la cicatrisation est obtenue. On conseille d’attendre deux à trois semaines pour la reprise progressive d’une activité sexuelle. Un arrêt de travail n’est le plus souvent pas nécessaire. On conseille d’attendre un à deux mois pour reprendre une activité type équitation ou cyclisme.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Le résultat</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Il ne peut être jugé qu’un mois après l’intervention. La vulve a alors une forme harmonieuse. Les cicatrices s’estompent en 1 à 2 mois. Le but de cette chirurgie est d’apporter une amélioration et non pas d’atteindre la perfection. Si vos souhaits sont réalistes, le résultat obtenu devrait vous donner une grande satisfaction.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Les imperfections du résultat</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Il s’agit essentiellement d’asymétries résiduelles, d’inadéquation de taille (résection insuffisante) ou de la persistance du prolongement antérieur. Dans ces cas, une correction chirurgicale secondaire peut être faite mais il convient d’attendre au moins 6 mois à 1 an.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">&nbsp;</span></div>
<div style="text-align: justify;">
<h3>Les risques et complications envisageables</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La nymphoplastie de réduction, bien que réalisée pour des motivations en partie esthétique, n’en reste pas moins une véritable intervention chirurgicale, ce qui implique les risques liés à tout acte médical, aussi minimes soient-ils.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Les suites opératoires sont en général simples au décours d’une nymphoplastie. Toutefois, des complications peuvent survenir, certaines d’ordre général, inhérentes à tout acte chirurgical, d’autres loco-régionales.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Il faut distinguer les complications liées à l’anesthésie de celles liées au geste chirurgical.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">En ce qui concerne l’anesthésie, lors de la consultation, le médecin-anesthésiste informera lui-même la patiente des risques anesthésiques. Il faut savoir que l’anesthésie induit dans l’organisme des réactions parfois imprévisibles, et plus ou moins faciles à maîtriser : le fait d’avoir recours à un Anes- thésiste parfaitement compétent, exerçant dans un contexte réellement chirurgical, fait que les risques encourus sont devenus statistiquement extrêmement faibles. Il faut savoir, en effet, que les techniques, les produits anesthésiques et les méthodes de surveillance ont fait d’immenses progrès ces trente dernières années, offrant une sécurité optimale, surtout quand l’intervention est réalisée en dehors de l’urgence et chez une personne en bonne santé.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">En ce qui concerne le geste chirurgical : en choisissant un Chirurgien Plasticien qualifié et compétent, formé à ce type d’intervention, vous limitez au maximum ces risques, sans toutefois les supprimer complètement.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Heureusement, les vraies complications sont rares à la suite d’une nymphoplastie de réduction réalisée dans les règles. En pratique, l’immense majorité des interventions se passe sans aucun problème et les patientes sont pleinement satisfaites de leur résultat.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Pour autant et malgré leur faible fréquence, vous devez être informée des complications possibles :</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Les accidents thrombo-emboliques (phlébite, embolie pulmonaire), bien que globalement très rares après ce type d’intervention, sont parmi les plus redoutables. Des mesures préventives rigoureuses doivent en minimiser l’incidence.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Un saignement est rare mais peut nécessiter une reprise rapide.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Un hématome peut nécessiter un geste d’évacuation</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• La survenue d’une infection est rare</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Un retard de cicatrisation voire même une désunion des berges des sutures peuvent parfois être observés, allongeant les suites opératoires.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Une nécrose de la muqueuse observée dans certaines techniques opératoires peut être responsable d’un retard de cicatrisation.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Une altération durable de la sensibilité est exceptionnelle.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Ainsi dans la très grande majorité des cas, cette intervention bien étudiée au préalable et correctement maîtrisée donne un résultat très appréciable en termes d’esthétique <span lang="EN-US">et de confort.</span></span></div>
