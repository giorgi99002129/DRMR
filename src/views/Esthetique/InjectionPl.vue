<h1>Injection de toxine botulique</h1>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Depuis 2003, les autorités administratives françaises chargées du contrôle des médicaments ont délivré une autorisation de mise sur le marché (A.M.M.) dans un but esthétique à la toxine botulique sous les noms de VISTABEL, AZZALURE et BOCOUTURE.&nbsp;</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Cette autorisation concerne l’utilisation esthétique pour le traitement des rides de la glabelle (rides inter-sourcilières ou «rides du lion») sous certaines conditions de compétence des praticiens et de modalités d’utilisation. Pour le cas où des injections seraient pratiquées dans le domaine de l’esthétique en dehors de ce site anatomique, elles seraient alors réalisées « hors A.M.M ». En pratique, les indications les plus classiques de la toxine botulique dans le domaine de l’esthétique concernent les rides inter-sourcilières, les rides du front et les rides de la patte d’oie. L’utilisation de la toxine botulinique a permis de réduire les indications chirurgicales au niveau du tiers supérieur de la face.&nbsp;<br><br></span></div>
<div style="text-align: justify;"></div>
<div style="text-align: justify;">
<h2>Objectif de l'injection botox&nbsp;</h2>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’objectif essentiel de ce traitement est de diminuer les rides et les ridules au repos et non d’empêcher la contraction musculaire : on jugera donc le résultat principalement sur l’aspect des rides au repos. En ce qui concerne la balance entre les muscles abaisseurs et élévateurs, la toxine botulique en atténuant l’action d’un groupe de muscle, atténue les rides dues à ces muscles mais libère l’action des muscles antagonistes : le but est de traiter la balance musculaire et d’obtenir ainsi une harmonisation du visage. Le traitement par toxine botulique doit être conçu et géré dans le temps : il convient donc de traiter progressivement et d’éviter le risque «d’en faire trop» lors des premières injections.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Il vaut mieux une première séance modérément efficace que trop efficace : il n’est donc pas souhaitable de vouloir un résultat optimal dès la première injection. Dans certains cas, il peut être nécessaire de pratiquer plusieurs séances d’injections avant d’adapter au mieux les possibilités du produit à chaque patient(e).<br></span></span><span style="font-family: tahoma, arial, helvetica, sans-serif;"><span style="font-family: tahoma, arial, helvetica, sans-serif;"><br></span></span>
<div>
<h3>Principes de la toxine botulique</h3>
</div>
<span style="font-family: tahoma, arial, helvetica, sans-serif;"><span style="font-family: tahoma, arial, helvetica, sans-serif;"></span></span>
<div><span style="font-size: 12pt;">Le principe de la toxine botulique consiste en l’utilisation des propriétés de relaxation musculaire de ce produit. Le but le plus souvent recherché est de réduire l’action des muscles situés au niveau du front et des sourcils, afin d’atténuer aussi bien les rides horizontales que les rides verticales provoquées par la contraction de ces muscles. Il ne s’agit pas d’un « comblement ». D’un point de vue plus global, il faut savoir que la position des sourcils est assurée par un équilibre entre deux forces opposées, constituées par des muscles abaisseurs (situés au voisinage des sourcils), et un muscle releveur (le muscle frontal). Les rides horizontales sont dues à la contraction du muscle frontal qui est releveur. Les rides verticales inter-sourcilières sont occasionnées par la contraction des muscles abaisseurs.</span></div>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><br><strong></strong></span></span>
<h3>Avant les injections</h3>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Aucune préparation particulière n’est nécessaire. Le jour de l’injection, vous n’avez pas besoin d’être à jeûn. Pour les femmes, il convient de prévoir de venir sans maquillage ou de l’enlever avant les injections. Il est très important de minimiser les risques de saignement des régions traitées en évitant de prendre de l’aspirine pendant les 15 jours qui précèdent les injections et les 15 jours qui les suivent. Il convient de respecter les contre-indications suivantes : <br>• Certaines maladies neuro-musculaires comme la myasthénie.<br> • La grossesse et l’allaitement : le fait d’être enceinte, même de quelques jours seulement, nécessite impérativement de repousser la date des injections après la grossesse et l’arrêt de l’allaitement éventuel. Il convient par conséquent en cas de doute d’effectuer les examens nécessaires et d’en communiquer les résultats à votre médecin.<br> • La toxine botulique est contre-indiquée en cas d’hypersensibilité connue à la neurotoxine botulinique A ou à la sérum-albumine, et de traitement par les aminosides (famille d’antibiotiques dont font partie notamment l’Amiklin et la Gentalline). Il convient aussi de respecter un certain nombre de précautions : <br>• Si vous êtes soigné(e) par ailleurs par des injections de toxine botulique pour des spasmes pathologiques, si vous avez eu un épisode de paralysie des muscles de la face, si vous souffrez d’une maladie neuro-musculaire ou de troubles de la coagulation, vous devez en faire part au praticien qui vous prend en charge pour ces injections de Toxine Botulique. Celui-ci jugera alors avec votre médecin traitant de l’opportunité des injections sur votre personne et de la manière de procéder s’il y a lieu.<br> • Si d’ici les injections, vous prenez des médicaments de type anti-coagulants ou aspirine ou antibiotiques, ou si vous avez un ennui de santé quelconque (notamment infection, grippe, abcès dentaire…) vous devez impérativement en faire part à votre médecin avant l’injection. <br>• D’une manière générale, vous ne devez pas hésiter à mentionner à votre praticien, le moindre problème de santé rencontré afin que celui-ci apprécie l’opportunité des injections. Dans le même ordre d’idée, vous devez lui faire part de toutes vos interrogations et mentionner tous les traitements dont vous avez pu faire l’objet ou dont vous faites encore l’objet.<br><br><strong></strong></span></span></span>
<h3>Type d'anesthésie</h3>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Aucune anesthésie n’est nécessaire.<br><br> <strong></strong></span></span></span>
<h3>Déroulement des injections botox</h3>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le traitement sera réalisé au cabinet du praticien ou dans une clinique selon le choix et les habitudes de votre médecin. Ce traitement consiste en une série d’injections au niveau du visage. L’aiguille est fine, et les injections sont habituellement peu douloureuses. La durée du traitement est de l’ordre de quelques minutes.<br><br><strong></strong></span></span>
<h3>Après les injections : Les suites</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Pendant les deux heures qui suivent les injections, il vous est recommandé de ne pas faire de sport intensif et de ne pas vous allonger. Il convient également d’éviter les manipulations du visage ou les massages appuyés pendant les 24 heures qui suivent la séance.</span><br><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Il est très important de minimiser les risques de bleus ou de saignement des régions traitées pendant les quinze jours qui précèdent et les quinze jours qui suivent les injections (afin de limiter le risque de fuite du produit vers les muscles périphériques ce qui risquerait d’entraîner des effets non désirés). Par ailleurs, pendant les trois jours qui suivent les injections, il est souhaitable de fortement contracter les muscles injectés trois fois par jour pendant environ 5 secondes par muscle. Habituellement, les suites de ces injections sont simples. Quelques marques un peu gonflées subsistent 20 à 30 minutes puis disparaissent. Les patient(e)s peuvent reprendre leurs activités normalement après les injections. L’utilisation de doses adaptées à chaque cas, en fonction notamment de la puissance musculaire permet d’éviter l’aspect « figé » souvent redouté par les patient(e)s. Quelques rares effets indésirables peuvent apparaître. Ils sont transitoires. <br>Il peut s’agir de :</span><br><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Rougeur : une rougeur localisée aux points d’injection a été parfois signalée et persiste rarement au delà de 3 à 6 jours. </span><br><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Ecchymoses («bleus») : des bleus sont en fait rarement observés au niveau des zones d’injection et peuvent perdurer quelques jours. </span><br><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Œdèmes : un gonflement, le plus souvent autour des yeux, peut s’installer progressivement en 4 à 5 jours, pour décroître ensuite en quelques jours à quelques semaines. </span><br><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Troubles de la sensibilité : une sensation de tension ou de fixité du front, de la bouche ou du cou, suivant les zones injectées, ainsi qu’une modification de la sensibilité souvent liée à une sensation de cartonnement peut persister plusieurs jours </span><br><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Douleurs fugaces oculaires ou faciales : de telles douleurs ont été décrites dans les zones injectées de manière tout à fait exceptionnelle. Quoiqu’il en soit, dans les suites de ces injections, n’hésitez surtout pas à recontacter votre praticien si vous avez la moindre inquiétude.</span></div>
