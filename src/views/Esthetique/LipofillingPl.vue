<h1>Lipofilling, lipostructure par injection de graisse</h1>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Dès que les premières lipoaspirations ont été réalisées, les Chirurgiens Plasticiens ont eu l’idée de réutiliser la graisse ainsi extraite pour la réinjecter en un autre endroit du corps, dans un but de comblement. Cette technique de réinjection de graisse autologue, appelée lipo-filling, s’est longtemps avérée décevante : la graisse réinjectée avait tendance à se résorber dans une proportion importante, rendant les résultats aléatoires et éphémères. Cependant les Chirurgiens Plasticiens ne se sont pas arrêtés à ces premières déceptions et ont tenté de comprendre les raisons de ces échecs. D’étape en étape, les résultats se sont améliorés, mais c’est surtout à partir de 1995 que la réinjection de graisse autologue, aussi appelée Lipostructure est devenue une méthode réellement fiable (S. COLEMAN). Des progrès déterminants concernant notamment le mode atraumatique de prélèvement et de réinjection ainsi que la purification de la graisse ont permis de limiter la part de résorption du tissu graisseux. La lipostructure peut s’appliquer à un grand nombre de dépressions (creux) naturelles, post-traumatiques ou iatrogéniques.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> <br></span></span>
<h2>Objectifs de l'intervention</h2>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">On peut résumer les objectifs et les indications de ce type d’intervention de la manière suivante : </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">1. Les indications d’ordre esthétique </span></div>
<div style="text-align: justify;">
<ul>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le comblement et l’atténuation de certaines rides, notamment au niveau du visage,</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La restauration de «la plénitude» d’un visage amaigri ou lors des premiers stades du vieillissement facial.</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La restauration des volumes et des formes du visage : il peut s’agir notamment de la restauration d’un visage émacié par le vieillissement.</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Un complément associé à certains liftings cervico-faciaux pour améliorer l’harmonie du visage.</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Secondairement, après un premier lifting, pour améliorer le galbe du tiers moyen de la face sans avoir recours à un nouveau lifting.</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La correction d’irrégularités secondaires à une lipoaspiration.</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le remodelage de la silhouette, encore appelé Lipomodelage : il consiste à prélever la graisse dans une zone où elle est excédentaire (culotte de cheval par exemple) et la réimplanter dans une zone où le volume manque (haut de la fesse par exemple). Le traitement de ces disgrâces esthétiques ne justifie pas une prise en charge par l’assurance maladie.</span></li>
</ul>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">2 - Les indications en chirurgie réparatrice et reconstructrice</span></div>
<div style="text-align: justify;">
<ul>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le comblement d’une dépression tissulaire à la suite d’un traumatisme,</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La correction des fontes graisseuses après trithérapie chez les patients HIV +.</span></li>
<li><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’amélioration esthétique des résultats de reconstruction mammaire après l’ablation de la glande mammaire, ou après mise en place d’implants mammaires.&nbsp;</span></li>
</ul>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Dans ces indications de chirurgie reconstructrice, la lipostructure peut être prise en charge par l’assurance maladie sous certaines conditions. Il faut cependant garder à l’esprit que la lipostructure doit toujours être considérée comme une véritable intervention chirurgicale qui doit être réalisée par un Chirurgien Plasticien&nbsp;compétent et qualifié, formé spécifiquement à ce type de technique et exerçant dans un contexte réellement chirurgical.</span></div>
<div style="text-align: justify;">
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">On aura pratiqué une étude minutieuse, clinique et photographique, des corrections à apporter. Un bilan pré-opératoire est réalisé conformément aux prescriptions. Le médecin-anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention. Le tabac ne constitue pas une contre-indication formelle mais son arrêt un mois avant l’intervention est recommandé compte-tenu de son incidence néfaste sur la cicatrisation. Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. </span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : La lipostructure est habituellement réalisée sous anesthésie locale approfondie par des tranquillisants administrés par voie intra-veineuse (anesthésie « vigile »). On peut aussi avoir recours à une anesthésie locale simple, voire à une anesthésie générale. Le choix entre ces différentes techniques sera le fruit d’une discussion entre vous, le chirurgien et l’anesthésiste. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : Cette chirurgie est le plus souvent réalisée en ambulatoire, l’entrée et la sortie se faisant le même jour. Il peut être indiqué de rester hospitalisé la nuit qui suit l’intervention.</span></div>
