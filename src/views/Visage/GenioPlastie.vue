								<div style="text-align: justify;">
<h1>La génioplastie</h1>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;"><span style="font-size: 12pt;">La génioplastie ou mentoplastie est une intervention qui a pour but la modification de la forme du menton et une amélioration fonctionnelle (respiration, sangle labiale). Elle peut être effectuée par ostéotomie (section osseuse), greffe ou mise en place d’un implant. <br><br></span></span>
<h2>Objectifs et Principes de la génioplastie</h2>
<span style="font-size: 12pt;">Cette intervention au niveau du menton est parfois associée à une chirurgie des mâchoires (ostéotomie maxillaire et/ou mandibulaire) ou à une rhinoplastie dans le cadre d’une profiloplastie. Le but est d’obtenir un menton d’aspect naturel s’harmonisant dans ses rapports avec les autres traits du visage, convenant à la psychologie et à la personnalité du patient et répondant aux demandes de ce dernier.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"><span style="font-size: 12pt;"><strong><br></strong></span></span>
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Les motivations et les demandes du (de la) patient(e) auront été analysées. Une étude attentive de la morphologie globale du visage aura été faite, ainsi qu’un examen endo-buccal.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Le résultat escompté pourra être «évalué» par photographies ou retouche informatiques. L’image virtuelle, ainsi obtenue ne constitue qu’un projet qui peut aider dans la compréhension des attentes des patient(e)s. Cependant, on ne peut en aucune manière s’engager à ce que le résultat réalisé lui soit en tout point superposable.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">L’arrêt du tabac (ou bien la réduction maximale de sa consommation) est vivement recommandé un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation).</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">On vous demandera de rester à jeun (ne rien manger ni boire) 6 heures avant l’intervention.<br><br></span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Type d’anesthésie : L’intervention se pratique sous anesthésie générale classique durant laquelle vous dormirez complètement.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Modalités d’hospitalisation : la durée d’hospitalisation habituelle est de 1 à 2 jours.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;"><span style="font-size: 12pt;"><strong><br></strong></span></span>
<h3>L’intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Dans la plupart des cas, le cas du menton est abordé par une incision au niveau de la muqueuse buccale. L’incision se trouve donc à l’intérieur de la bouche au niveau de la lèvre inférieure. Il n’y a pas de cicatrice extérieure.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Le chirurgien coupe le menton au dessous des racines des dents lors de l’ostéotomie ce qui permet de déplacer le fragment osseux dans la direction prévue avant l’intervention. Ce fragment est ensuite fixé par des vis ou des mini-plaques (ostéosynthèse).</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Des points de sutures sont ensuite mis en place dans la bouche et disparaissent en 2 à 4 semaines ou sont parfois retirés en consultation.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Dans certains cas, la correction de l’anomalie du menton peut nécessiter une greffe osseuse (prélevée le plus souvent sur le crâne ou plus rarement au niveau du bassin).</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">La modification peut parfois être obtenue par un implant (prothèse) mis en place par une incision réalisée dans la bouche. La consolidation osseuse est obtenue en un mois et demi.</span></div>
<div style="text-align: justify;">
<h3>Après l'intervention : Suites opératoires</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Des saignements par la bouche sont fréquents juste après l’intervention et sont habituellement sans gravité. Un œdème des lèvres et du cou est assez fréquent et parfois important. La mobilité est presque toujours diminuée au début. La sensibilité peut être aussi diminuée dans les premiers jours au niveau de la lèvre inférieure, du menton, des dents et des gencives. Les douleurs seront soulagées par des antalgiques.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Une excellente hygiène buccale est essentielle et des bains de bouche vous seront prescrits à cet effet. Les dents et les gencives doivent être nettoyées avec une brosse ultra-souple après chaque repas ; un gel hydropulseur peut également être utilisé.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Dans certains cas, des antibiotiques vous seront prescrits.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Pour obtenir une cicatrisation dans de bonnes conditions, il faut respecter certaines précautions : l’alimentation doit être tiède ou froide, plutôt molle en évitant une nourriture trop chaude, trop épicée ou trop acide.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Le déplacement du menton modifie le visage de façon plus ou moins importante. Il est cependant nécessaire de surveiller le résultat de l’intervention après fonte de l’œdème par des examens de contrôles réguliers.</span></div>
<div style="text-align: justify;">
<h3>Le résultat</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Un délai de deux à trois mois est nécessaire pour avoir une bonne appréciation du résultat, en sachant que l’aspect définitif est généralement obtenu après environ six mois.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Dans certains cas, le matériel d’ostéosynthèse doit être retiré au cours d’une autre intervention 6 mois au moins après la première.</span></div>
<div style="text-align: justify;">
<h3>Les imperfections de résultat</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Elles peuvent résulter d’un malentendu concernant les buts à atteindre ou survenir du fait de phénomènes cicatriciels inhabituels ou de réactions tissulaires inattendues.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Ces petites imperfections, si elles sont mal supportées, pourront éventuellement être corrigées par une retouche chirurgicale, en général beaucoup plus simple que l’intervention initiale, tant du point de vue technique que des suites opératoires. Une telle retouche ne peut toutefois pas être réalisée avant plusieurs mois afin d’agir sur des tissus stabilisés et ayant atteint une bonne maturation cicatricielle.</span></div>
<div style="text-align: justify;">
<h3>Les risques et complications envisageables</h3>
</div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Une génioplastie, bien que réalisée pour des motivations essentiellement esthétiques, n’en reste pas moins une véritable intervention chirurgicale, ce qui implique les risques liés à tout acte médical, aussi minime soit-il.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Il convient de distinguer les complications liées à l’anesthésie de celles liées au geste chirurgical.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">En ce qui concerne l’anesthésie, lors de la consultation, le médecin anesthésiste informera lui-même le patient des risques anesthésiques. Il faut savoir que l’anesthésie induit dans l’organisme des réactions parfois imprévisibles, et plus ou moins faciles à maîtriser : le fait d’avoir recours à un Anesthésiste parfaitement compétent, exerçant dans un contexte réellement chirurgical, fait que les risques encourus sont devenus statistiquement très faibles.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Il faut savoir, en effet, que les techniques, les produits anesthésiques et les méthodes de surveillance ont fait d’immenses progrès ces trente dernières années, offrant une sécurité optimale, surtout quand l’intervention est réalisée en dehors de l’urgence et chez une personne en bonne santé.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">En ce qui concerne le geste chirurgical : en choisissant un Chirurgien Plasticien qualifié et compétent, formé à ce type d’intervention, vous limitez au maximum ces risques, sans toutefois les supprimer complètement.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Heureusement, les vraies complications sont rares à la suite d’une génioplastie réalisée dans les règles. En pratique, l’immense majorité des interventions se passe sans aucun problème et les patient(e)s sont pleinement satisfait(e)s de leur résultat.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Pour autant, et malgré leur rareté, vous devez être informé(e) des complications possibles :</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">•Saignements : ils peuvent être abondants, ce qui est extrêmement rare à la suite de cette intervention. Ils peuvent nécessiter très exceptionnellement une transfusion sanguine ou de dérivés sanguins.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">En cas de saignements très importants, il peut être nécessaire d’ouvrir la plaie en urgence et de réaliser une hémostase chirurgicale. Un hématome important peut comprimer la langue et entraîner des troubles respiratoires.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">•Les troubles de la sensibilité de la lèvre inférieure, de la gencive ou des dents, sont habituels et s’estomperont progressivement mais la récupération peut être longue.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">•Retard ou absence de consolidation osseuse Dans ce cas parfois, une autre intervention avec greffe osseuse</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">peut être nécessaire.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">•Lésions dentaires : dans des cas exceptionnels, les racines dentaires peuvent être lésés et nécessiter un traitement (résection lapicale)</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">•Fonte du fragment osseux : très rarement le fragment osseux découpé peut se résorber progressivement entraînant la disparition du relief créé par l’intervention. Une reconstruction secondaire nécessitera alors une greffe osseuse ou la mise en place d’un implant mentonnier.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">•Complications en cas de pose d’implant mentonnier :</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">Les implants artificiels (silicone, corail etc...) peuvent être mal tolérés ou s’infecter nécessitant alors, en raison de leur dégradation et/ou de leur mobilité, une ablation et une reconstruction secondaire.</span></div>
<div style="text-align: justify;"><span style="font-size: 12pt;">&nbsp;Au total, il ne faut pas surévaluer les risques, mais simplement prendre conscience qu’une intervention chirurgicale, même apparemment simple, comporte toujours une petite part d’aléas.</span></div>
<div style="text-align: justify;"><span lang="EN-US" style="font-size: 12pt;">Le recours à une Chirurgien Plasticien qualifié vous assure que celui-ci a la formation et la compétence requises pour savoir éviter ces complications, ou les traiter efficacement le cas échéant.</span></div>
