<h1>Otoplastie: Chirurgie des oreilles décollées</h1>
<!--3h3--></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> La correction d’oreilles décollées nécessite une intervention chirurgicale, appelée «otoplastie », visant à remodeler les pavillons jugés excessivement visibles. L’opération est habituellement réalisée sur les deux oreilles, mais peut parfois être unilatérale. Une otoplastie se propose de corriger les anomalies du cartilage présentes au niveau du pavillon de l’oreille et responsables de son aspect «décollé». <br>On peut schématiquement distinguer trois types de malformations qui sont souvent plus ou moins associées entre elles : <br></span></span>
<ul>
<li><span style="vertical-align: super; font-size: 12pt;">Angulation trop importante entre le pavillon de l’oreille et le crâne réalisant le vrai «décollement» (Hélix Valgus).&nbsp;</span></li>
<li><span style="vertical-align: super; font-size: 12pt;">Taille excessive du cartilage de la conque projetant l’oreille vers l’avant, ce qui accentue l’aspect décollé (hypertrophie de la conque).&nbsp;</span></li>
<li><span style="font-size: 12pt;">Un défaut de plicature des reliefs habituels du cartilage qui fait que le pavillon de l’oreille a un aspect trop lisse, comme «déplissé» (défaut de plicature de l’anthélix).</span><span style="font-size: 12pt;"> </span></li>
</ul>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Une otoplastie peut être réalisée chez l’adulte ou l’adolescent(e), mais la plupart du temps la correction est envisagée dès l’enfance où elle peut être pratiquée à partir de l’âge de 7 ans, dès lors que l’enfant en affirme le désir. Cette chirurgie peut surtout chez l’enfant et si la disgrâce n’est pas mineure, bénéficier d’une participation financière de l’assurance maladie. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><strong><br></strong></span></span>
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Un examen attentif des oreilles ainsi qu’un bilan photographique auront été réalisés par le chirurgien afin d’analyser les modifications à apporter. Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions En cas d’anesthésie autre que purement locale, le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention. L’intervention vise à corriger définitivement ces anomalies en remodelant le cartilage, de façon à obtenir des oreilles « recollées », symétriques, de taille et d’aspect naturels. Elle permet ainsi de mettre fin aux moqueries et autres remarques désobligeantes susceptibles d’être à l’origine de difficultés psychologiques ou de conflits scolaires. Société Française de Chirurgie Plastique Reconstructrice et Esthétique </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><strong><br></strong></span></span>
<h3>Précautions à prendre</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. Une coupe de cheveux bien courte ou une coiffure dégageant les oreilles seront à prévoir pour le jour de l’intervention (Pour les filles, une queue de cheval sera la bienvenue). La tête et les cheveux seront soigneusement lavés la veille de l’opération. En fonction du type d’anesthésie, il faudra être à jeun (ne rien manger ni boire) 6 heures avant l’intervention.<br><br></span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : Trois procédés sont envisageables :</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Anesthésie locale pure, où un produit anesthésique est injecté localement afin d’assurer l’insensibilité des oreilles. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Anesthésie locale approfondie par des tranquillisants administrés par voie intra-veineuse (anesthésie « vigile»).</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Anesthésie générale classique, durant laquelle vous dormez complètement.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Le choix entre ces différentes techniques sera le fruit d’une discussion entre vous, le chirurgien et l’anesthésiste. Modalités d’hospitalisation : Habituellement l’intervention se pratique en «ambulatoire», c’est-à-dire en hospitalisation de jour, avec une sortie autorisée le jour même après quelques heures de surveillance. Toutefois, dans certains cas, une courte hospitalisation peut être préférée. Il peut être indiqué de rester hospitalisé la nuit qui suit l’intervention.</span></div>
