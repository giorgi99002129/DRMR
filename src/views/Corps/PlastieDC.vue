                    <div style="text-align: justify;">
<h1>Plastie des cicatrices</h1>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Une cicatrice est la partie visible d’une lésion du derme après que le tissu se soit réparé, suite à une incision au cours d’une opération ou après une blessure. La cicatrisation fait partie intégrante du processus de guérison. A part les lésions très mineures, chaque blessure (après un accident, une maladie, ou un acte chirurgical) engendre une cicatrice plus ou moins importante. Le tissu cicatriciel n’est pas identique au tissu qu’il remplace et est habituellement de qualité fonctionnelle inférieure. Par exemple, les cicatrices cutanées sont plus sensibles au rayonnement ultraviolet, les glandes sudoripares et les follicules pileux ne se développent pas sous la cicatrice. Cependant, quelques tissus (par exemple l’os) peuvent guérir sans détérioration structurelle ou fonctionnelle. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Les seules agressions infligées à la peau qui disparaissent sans laisser aucune cicatrice sont celles qui ne concernent que la partie la plus superficielle de la peau à savoir l’épiderme. Dès qu’une incision traverse le derme c’est-à-dire la partie profonde de la peau, et quelle que soit la qualité du chirurgien et des soins par lui prodigués, le geste chirurgical laissera derrière lui une cicatrice qui, certes, ira en s’atténuant, devenant plus ou moins discrète, mais jamais totalement invisible. Nous aborderons le traitement des cicatrices lorsque celles-ci, anormales ou inesthétiques, requièrent une prise en charge chirurgicale.<br><br> </span></div>
<div style="text-align: justify;">
<h2>Objectif de l'intervention</h2>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Il est impossible de faire disparaître complètement une cicatrice, que ce soit par la chirurgie ou par tout autre moyen (pommades, tatouages, peeling, laser…). Dans la plupart des cas, ne peuvent être traitées que les cicatrices stabilisées, au terme de leur évolution naturelle (6 mois à 2 ans). En effet la cicatrisation va se faire principalement en trois phases. La première phase débute quelques jours après l’intervention ; la cicatrice est généralement belle et fine. Ensuite, au cours des 1 à 2 mois post-opératoires, elle devient plus rouge, dure, boursouflée et des démangeaisons sont fréquentes. Ce stade dure en moyenne 3 à 6 mois. La cicatrice rentre ensuite dans sa dernière phase où elle commence à blanchir et à s’assouplir. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">De la même façon, les démangeaisons disparaissent. Au terme de ce processus plus ou moins long et imprévisible, la cicatrice est indélébile, blanche, souple et insensible. Durant toute son évolution, il est important de prendre soin de votre cicatrice afin qu’elle devienne la plus discrète possible : il faut la protéger du soleil (vêtements, chapeau, crème solaire protectrice), l’hydrater avec des crèmes et la masser quotidiennement. Dans certains cas votre chirurgien peut vous prescrire du silicone en plaque ou en gel afin de diminuer les rougeurs ou l’aspect boursouflé. Les tentatives de correction chirurgicale ne sont licites que dans les cas suivants :</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Cicatrices rétractiles : très indurées et «recroquevillées», ne se laissant pas distendre, très inesthétiques et pouvant mêmeparfois limiter certains mouvements, </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Cicatrices ulcérées : leur fragilité aboutit à de fréquentes «écorchures» superficielles qui deviennent permanentes, se creusent et s’aggravent,</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Cicatrices hypertrophiquesouchéloïdiennes : inflammatoires, douloureuses, rouges, élargies et surtout gonflées «en relief». Leur traitement est très délicat et soumis à de fréquentes récidives, </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• &nbsp;Cicatrices inesthétiques : larges, colorées, irrégulières, décalées, enfoncées, adhérentes.<br><br> </span></div>
<div style="text-align: justify;">
<h3>Principe de l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Dans les cas les plus simples, il suffit d’exciser la cicatrice défectueuse et de refermer avec une technique de suture parfaite pour espérer obtenir une nouvelle cicatrice plus discrète. Souvent, il est nécessaire d’avoir recours à un tracé d’incision spécial visant à «briser» l’axe principal de la cicatrice initiale, à réorienter au mieux la cicatrice en fonction des lignes de tension naturelles de la peau, et à diminuer ainsi la tension exercée sur les berges de la plaie. Pour les cicatrices très étendues, plusieurs techniques peuvent être employées, seules ou en association :</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Excision en plusieurs fois, permettant à la peau de se détendre entre deux temps opératoires (principe dit des «exé- rèses itératives») </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Greffe de peau prélevée sur une autre région</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Plastie locale, avec déplacement d’un lambeau de peau avoisinant afin que celui-ci vienne recouvrir la zone cicatricielle</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Expansion cutanée, grâce à des ballonnets gonflables placés sous la peau saine avoisinante et permettant de la distendre au fur et à mesure du gonflement (sur plusieurs semaines) afin d’utiliser l’excédent cutané ainsi obtenu lors de l’ablation des ballons pour recouvrir la zone cicatricielle. Dans tous les cas, votre chirurgien vous expliquera quelle est la solution la mieux adaptée à votre cas personnel.<br><br> </span></div>
<div style="text-align: justify;">
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Un interrogatoire suivi d’un examen de la cicatrice incriminée aura été réalisé par le chirurgien afin de préciser la ou les possibilité(s) chirurgicale(s). En cas d’anesthésie autre que purement «locale», un bilan pré-anesthésique pourra être prescrit et une consultation pré-opératoire sera obligatoirement prévue avec le médecin anesthésiste. L’arrêt du tabac est recommandé un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation). Aucun médicament contenant de l’Aspirine ne devra être pris dans les 10 jours précédant l’intervention. En fonction du type d’anesthésie, on pourra vous demander de rester à jeun (rien manger ni boire) 6 heures avant l’intervention. Aucun maquillage ni aucun bijou ou piercing ne devra être porté durant l’opération.<br><br> </span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Type d’anesthésie : Trois procédés sont envisageables</span><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">: <br>• Anesthésie locale pure, où un produit analgésique est injecté afin d’assurer l’insensibilité de la zone à opérer. C’est le cas le plus fréquent pour la Dermato-Chirurgie de base.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">• Anesthésie «vigile» (Anesthésie locale approfondie par des tranquillisants), durant laquelle vous pouvez rester éveillé mais où vous serez relaxé et dont il peut résulter une certaine amnésie de l’intervention. Elle peut être préférée pour des raisons de confort personnel ou pour la réalisation de certains lambeaux complexes, en particulier sur la face.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> • Anesthésie générale classique, durant laquelle vous dormez complètement, en fait rarement utile en Dermato-Chirurgie. Le choix entre ces différentes techniques sera le fruit d’une discussion entre vous, le chirurgien et l’anesthésiste. Modalités d’hospitalisation : les interventions de DermatoChirurgie de base, surtout si elles sont prévues sous anesthésie locale pure, ne nécessitent pas obligatoirement une hospitalisation et peuvent, à l’instar des soins dentaires, être réalisés en Cabinet, dans la mesure où tout l’équipement&nbsp;nécessaire y est disponible. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Si l’intervention est prévue en Clinique ou à l’Hôpital, elle peut habituellement être pratiquée «en externe», c’est-à-dire avec une entrée juste avant l’opération et une sortie juste après celle-ci, ou «en ambulatoire», c’est-à-dire en «hospitalisation de jour» avec une sortie le jour même après quelques heures de surveillance. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’hospitalisation traditionnelle, avec une nuit à passer sur place, est exceptionnelle pour ce type de chirurgie.&nbsp;</span></div>
