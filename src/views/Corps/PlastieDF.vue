                    <div style="text-align: justify;">
<h1>Plastie des fesses</h1>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> La projection de la fesse est le paramètre essentiel de la beauté des fesses. Elle est la résultante de la masse du muscle grand fessier apparue suite au redressement de l’Homme devenu bipède, associée à la «cambrure» de la chute des reins. L’idéal universel c’est d’avoir des fesses pleines et une taille fine (silhouette en sablier). </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La région de la fesse est influencée par quatre composants anatomiques : </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">- La colonne vertébrale : la position plus ou moins en avant ou en arrière du bassin influence la projection des fesses.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> - Le muscle grand fessier se développe par une activité musculaire soutenue ; il peut être «camouflé» par une position en avant du bassin.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> - La localisation graisseuse autour de la fesse est plus importante chez la femme que chez l’homme (morphologie gynoïde ou androïde). Elle définira la morphologie ethnique (asiatique, caucasienne, latino ou africaine). Cette graisse joue le rôle de «rembourrage» dans la position allongée sur le dos et de réserve énergétique; sa fonte détermine le vieillissement de la fesse. <br>- Enfin, la qualité de la peau ou son relâchement (ptose fessière) peuvent orienter plutôt vers un lifting fessier. L’esthétique de la fesse est variable en fonction des origines ethniques des patient(e)s. En effet, les asiatiques préfèrent des fesses petites, bien galbées, n’aiment pas la plénitude fessière ou trochantérienne. Les caucasiens aiment les fesses fermes et musclées. Les «latinos» aiment la plénitude fessière et peu de plénitude trochantérienne. Enfin, les africains ont un idéal culturel pour les fesses aussi fortes que possible et l’extrême proéminence de la chute des reins avec une plénitude aussi bien fessière que trochantérienne.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> <br></span></span>
<h2>Objectif de la plastie des fesses</h2>
<span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><br>La «fesse plate» est souvent mal acceptée physiquement et psychologiquement par le ou la patient(e), avec pour corollaire une altération de la confiance en soi et un mal-être, parfois profond, pouvant aller jusqu’au véritable complexe. C’est pourquoi, l’intervention se propose d’accroître le volume d’une fesse jugé trop petit grâce à l’implantation de prothèses. L’intervention peut se pratiquer à tout âge à partir de 18 ans. Un(e) patient(e) mineur(e) n’est habituellement pas estimé(e) apte à subir une augmentation fessière esthétique. Cette chirurgie à visée purement esthétique ne peut bénéficier d’une prise en charge par l’Assurance Maladie.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Les implants de fesses actuellement utilisés sont composés d’une enveloppe et d’un produit de remplissage. Ils diffèrent des implants mammaires par une enveloppe plus épaisse et un gel de remplissage plus cohésif. L’enveloppe est toujours constituée d’un élastomère de silicone. L’implant est dit «prérempli» car le produit de remplissage a été incorporé en usine. La gamme des différents volumes est donc fixée par le fabricant.<br><br></span></div>
<div style="text-align: justify;">
<h3>Les implants préremplis de gel de silicone de nouvelle génération pour la plastie des fesses&nbsp;</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> La grande majorité des prothèses actuellement posées en France et dans le monde sont pré-remplies de gel de silicone. Ces implants, utilisés depuis plus de 40 ans, ont fait la preuve de leur innocuité et de leur excellente adaptation à ce type de chirurgie car ils sont très proches de la consistance d’une&nbsp;fesse normale. ils ont par ailleurs bien évolué, en particulier à la fin des années 1990, afin de corriger les faiblesses que l’on pouvait leur reprocher. Aujourd’hui, tous les implants disponibles en France sont soumis à des normes précises et rigoureuses : marquage CE (Communauté Européenne) + autorisation de l’ANSM (Agence Nationale de Sécurité du Médicament et des produits de santé). </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Les évolutions marquantes des nouveaux implants, leur conférant une meilleure fiabilité, concernent tant les enveloppes que le gel lui-même : les enveloppes, dont la paroi est aujourd’hui beaucoup plus solide, empêchent la «transpiration» du gel vers l’extérieur (qui était une source importante de coques) et ont une résistance à l’usure très supérieure ; les gels de silicone «cohésifs», dont la consistance est moins fluide, limitent beaucoup le risque de « se répandre » en cas de rupture de l’enveloppe. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">À côté de cette amélioration de la fiabilité, la nouvelle génération d’implants en silicone se caractérise aussi par la grande diversité de formes actuellement disponibles, permettant une adaptation personnalisée à chaque cas. Ainsi, à côté des classiques prothèses rondes sont apparus des implants «anatomiques» profilés en forme de goutte d’eau, plus ou moins hauts, larges ou projetés. Cette diversité de formes, associée à un large choix de volumes, permet d’optimiser et d’adapter le choix, presque «sur mesure», des prothèses en fonction de la morphologie du (de la) patient(e) et de ses attentes personnelles.<br><br></span></div>
<div style="text-align: justify;">
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Un interrogatoire suivi d’un examen attentif aura été réalisé par le chirurgien qui prendra en compte tous les paramètres qui font de chaque patient(e) un cas particulier (taille, poids, morphologie, qualité de la peau, importance de la musculature et de la graisse…). En fonction du contexte anatomique, des préférences et des habitudes du chirurgien, et des désirs exprimés par le (la) patient(e) une stratégie opératoire aura été convenue. Seront ainsi prédéterminés l’emplacement des cicatrices, le type et la taille des implants. Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions. Le médecin anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention. Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. L’arrêt du tabac est vivement recommandé un mois avant et un mois après l’intervention (le tabac peut être à l’origine d’un retard de cicatrisation). On vous demandera de rester à jeun (ne rien manger ni boire) 6 heures avant l’intervention.<br><br> </span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : L’intervention se pratique sous anesthésie générale classique durant laquelle vous dormirez complètement. Dans de rares cas, une anesthésie « vigile» (anesthésie locale approfondie par des tranquilisants administrés par voie intraveineuse) pourra cependant être utilisée (à discuter avec le chirurgien et l’anesthésiste). </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : l’intervention justifie habituellement une hospitalisation d’une journée. L’entrée s’effectue alors le matin de l’intervention (ou parfois la veille dans l’après-midi) et la sortie est autorisée dès le lendemain. Toutefois, dans certains cas, l’intervention peut se pratiquer en «ambulatoire» c’est-à-dire avec une sortie le jour même après quelques heures de surveillance.</span></div>
