<h1>Abdominoplastie: C'est quoi</h1>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Dans le domaine de la correction de la paroi abdominale, il n’y a pas une seule technique qui puisse être appliquée à tous les cas. Il convient de bien analyser les lésions et de savoir prendre en compte plusieurs paramètres : état de la peau, importance de la surcharge graisseuse, tonicité des muscles abdominaux, morphologie générale ainsi que la demande et les attentes du (de la) patient(e). On en déduira alors la stratégie la mieux adaptée à chaque cas. Schématiquement, en présence d’une demande de correction chirurgicale de la paroi abdominale, deux cas de figure peuvent être observés: soit une lipoaspiration abdominale sera envisagée isolément, soit il faudra avoir recours à une plastie abdominale ou abdominoplastie. Elle est indiquée quand le seul problème est une surcharge graisseuse. Se reporter dans ce casà la fiche d’information concernant la <a href="/web/20190213050246/https://www.paris-esthetics.com/fr/categories/Liposaktciya">lipoaspiration</a>.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Chaque fois qu’il existe des lésions importantes de la peau (perte de tonicité, distention notable, vergetures importantes, cicatrices...) et/ou des altérations de la paroi musculaire (relâchement, diastasis, hernies...) la lipoaspiration isolée sera insuffisante et il faudra alors recourir à une plastie abdominale ou abdominoplastie. La plastie abdominale reste une intervention assez lourde en chirurgie plastique, mais elle a pleinement bénéficié de nombreuses améliorations techniques ces dernières années: allègement des procédés anesthésiques, techniques dites «à haute tension supérieure», méthodes de capitonnage, perfectionnement des pratiques de sutures, progrès des pansements et des gaines compressives etc.. Ce savoir-faire a permis de réduire significativement les risques, d’alléger les suites opératoires, de bien améliorer la qualité des résultats et d’optimiser la discrétion des cicatrices, ouvrant ainsi les indications à des cas plus «légers» qui, autrefois, auraient pu être récusés.<br><br> </span></div>
<div style="text-align: justify;">
<h2>Objectifs et principes d'intervention</h2>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Le but d’une telle intervention est d’enlever la peau la plus abîmée (distendue, cicatricielle ou vergeturée) et de retendre la peau saine périphérique. On peut y associer dans le même temps le traitement d’une surcharge graisseuse localisée par lipoaspiration et le traitement de lésions des muscles abdominaux sous-jacents (diastasis, hernie). Chaque fois qu’il existe une surcharge pondérale, celle-ci devra être corrigée au mieux (partiellement ou totalement) avant le geste chirurgical (notion de contrat de poids). Les conditions de l’intervention en seront meilleures aussi bien en ce qui concerne la sécurité que la qualité des résultats. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">La plastie abdominale étendue classique : L’abdominoplastie la plus habituellement réalisée consiste à&nbsp;pratiquer l’ablation d’un large fuseau de peau, correspondant à tout ou partie de la région située entre l’ombilic et le pubis, selon un dessin adapté aux lésions. La peau sus-jacente, saine, située en règle générale au-dessus de l’ombilic, sera redrapée vers le bas, de manière à reconstituer une paroi abdominale avec une peau de bonne qualité. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">L’ombilic est conservé et replacé en position normale, grâce à une incision faite dans la peau abaissée. Le plus souvent, cette cicatrice est située au bord supérieur des poils pubiens et remonte plus ou moins loin dans les plis de l’aine. Sa longueur est en grande partie prévisible avant l’intervention et le (la) patient(e) devra en être très clairement prévenu(e) car cette «rançon cicatricielle» reste un des inconvénients principaux qu’il faudra assumer. Une telle plastie abdominale étendue peut parfois bénéficier d’une participation financière de l’assurance maladie dans certains cas et sous certaines conditions. Les plasties abdominales localisées : En présence de lésions moins importantes, on pourra proposer parfois une plastie abdominale plus localisée dont la rançon cicatricielle sera réduite. Dans ces cas, la prise en charge par l’assurance maladie ne peut pas être envisagée.<br><br> </span></div>
<div style="text-align: justify;">
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Un bilan pré-opératoire habituel est réalisé conformément aux prescriptions. Le médecin-anesthésiste sera vu en consultation, au plus tard 48 heures avant l’intervention. Le tabagisme aggrave le risque de complication post-opératoire de toute intervention chirurgicale. Arrêter de fumer 6 à 8 semaines avant l’intervention supprime ce sur-risque. Si vous fumez, parlez-en à votre chirurgien et votre anesthésiste. Vous pouvez aussi téléphoner à Tabac-Info-Service (3989) pour vous faire aider à réduire le risque et mettre toutes les chances de votre côté. L’arrêt d’une éventuelle contraception orale peut être requis, notamment en cas de facteur de risques associés (obésité, mauvais état veineux; trouble de la coagulation). Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. Une préparation cutanée (type savon antiseptique) est habituellement recommandée la veille et le matin de l’intervention. Il est fondamental de rester à jeun (ne rien manger, ni boire) 6 heures avant l’intervention.</span></div>
<div style="text-align: justify;">
<h3>Type d'anesthésie et modalités d'hospitalisation&nbsp;</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Type d’anesthésie : L’abdominoplastie nécessite pratiquement toujours une anesthésie générale, durant laquelle vous dormez complètement. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : La durée d’hospitalisation varie de 2 à 5 jours</span></div>
