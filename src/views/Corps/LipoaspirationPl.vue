<h1>Lipoaspiration: c'est quoi</h1>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Ces surcharges graisseuses localisées ne disparaissent pas, en règle générale, malgré un régime alimentaire ou l’exercice physique. Par contre, la lipoaspiration ne constitue pas une méthode d’amaigrissement et son but n’est donc pas de maîtriser le poids du patient ou de la patiente : ainsi une lipoaspiration correctement réalisée ne permettra pas de faire l’économie d’une amélioration de l’hygiène de vie. La lipoaspiration ne constitue pas le traitement de l’obésité.<br><br></span></span>
<h2>Objectifs et principe de l'intervention</h2>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Le principe de la lipoaspiration (mis au point à partir de 1977 par Yves-Gérard ILLOUZ) est d’introduire, à partir de très petites incisions, des canules mousses, à bout arrondi, non tranchant, perforées à leur extrémité de plusieurs orifices. Ces canules seront connectées à un circuit fermé dans lequel sera créé une pression négative. C’est ainsi que sera possible l’aspiration harmonieuse et non traumatisante des cellules graisseuses en surnombre. Dans la mesure où ces cellules graisseuses n’ont pas la faculté de se re-multiplier, il n’y aura pas de récidive de cette surpopulation d’adipocytes. En pratique, la lipoaspiration peut s’appliquer à un grand nombre de région du corps : « la culotte de cheval» bien sûr, mais aussi les hanches, l’abdomen, les cuisses, les genoux, les mollets, les chevilles, les bras, le dos. Les améliorations techniques ont aussi permis d’étendre son action au niveau du visage et du cou (double menton, ovale du visage).</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Les récents progrès, notamment dans le domaine de la liposuccion superficielle, grâce à l’utilisation de canules très fines font que la peau sus-jacente à la zone traitée n’a plus à souffrir de la lipoaspiration : au contraire, l’aspect de la peau peut être amélioré par la rétraction cutanée que génère une lipoaspiration superficielle correctement réalisée.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Cependant, il faut garder à l’esprit que la lipoaspiration, malgré son extrême banalisation au cours de ces dernières années, doit toujours être considérée comme une véritable intervention chirurgicale qui ne peut être légalement pratiquée en France que par un chirurgien plasticien compétent et qualifié, formé spécifiquement à ce type de technique et exerçant dans un contexte réellement chirurgical. Le traitement de telles surcharges graisseuses localisées ne justifie pas une prise en charge par l’assurance maladie. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><strong><br></strong></span></span>
<h3>Avant l'intervention</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Un bilan pré-opératoire est réalisé conformément aux prescriptions. Le médecin-anesthésiste sera vu en consultation au plus tard 48 heures avant l’intervention. Le tabac ne constitue pas une contre-indication formelle mais son arrêt un mois avant l’intervention est recommandé compte-tenu de son incidence néfaste sur la cicatrisation.</span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> L’arrêt d’une éventuelle contraception orale peut être requis, notamment en cas de facteurs de risques associés (obésité, mauvais état veineux, trouble de la coagulation). Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention. En fonction du type d’anesthésie, on pourra vous demander de rester à jeun (ne rien manger ni boire) 6 heures avant l’intervention. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"><strong><br></strong></span></span>
<h3>Type d'anesthésie et modalités d'hospitalisation</h3>
</div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;"> Type d’anesthésie : La lipoaspiration peut être réalisée, selon les cas et l’importance des zones à traiter, soit sous anesthésie locale, soit sous anesthésie locale approfondie par des tranquillisants administrés par voie intra-veineuse (anesthésie «vigile»), soit sous anesthésie générale, éventuellement sous anesthésie loco-régionale (péridurale ou rachi-anesthésie). Le choix entre ces différentes techniques sera le fruit d’une discussion entre vous, le chirurgien et l’anesthésiste. </span></div>
<div style="text-align: justify;"><span style="font-family: tahoma, arial, helvetica, sans-serif; font-size: 12pt;">Modalités d’hospitalisation : La durée de l’hospitalisation est fonction de la quantité de graisse extraite.&nbsp;Elle peut être courte, de l’ordre de quelques heures pour de petites lipoaspirations (lorsqu’on a eu recours à une anesthésie locale). Elle sera de 1 ou 2 jours en cas de lipoaspiration plus importante (réalisée sous anesthésie générale).</span></div>
